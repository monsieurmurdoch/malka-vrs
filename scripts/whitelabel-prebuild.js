#!/usr/bin/env node

/**
 * Whitelabel Prebuild Script
 *
 * Reads the tenant config (TENANT env var, defaults to "malka") and generates:
 *   - css/_tenant-variables.scss  — SCSS color overrides for the tenant
 *   - whitelabel-runtime.js       — JS runtime config (appName, features, assets)
 *   - title.html                  — Branded <title> and meta tags
 *
 * Also copies referenced tenant assets from whitelabel/{tenant}/assets/ into images/.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = path.resolve(__dirname, '..');
const WHITELABEL_DIR = path.join(PROJECT_ROOT, 'whitelabel');

// ---------------------------------------------------------------------------
// Resolve tenant
// ---------------------------------------------------------------------------
const tenantId = process.env.TENANT || 'malka';
const configPath = path.join(WHITELABEL_DIR, `${tenantId}.json`);

if (!fs.existsSync(configPath)) {
    console.error(`[whitelabel] ERROR: Tenant config not found: ${configPath}`);
    console.error(`[whitelabel] Available tenants: ${listTenants().join(', ')}`);
    process.exit(1);
}

const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
console.log(`[whitelabel] Building for tenant: ${tenantId} (${config.brand.appName})`);

// ---------------------------------------------------------------------------
// 1. Generate css/_tenant-variables.scss
// ---------------------------------------------------------------------------
function generateSCSS(theme) {
    const lines = [
        '// Auto-generated by whitelabel-prebuild.js — DO NOT EDIT',
        `// Tenant: ${tenantId}`,
        '',
        `$malkaRoyalPurple: ${theme.primary};`,
        `$malkaLightPurple: ${theme.primaryLight};`,
        `$malkaDarkPurple: ${theme.primaryDark};`,
        `$malkaWhite: ${theme.surfaceLight};`,
        `$malkaDarkGrey: ${theme.surfaceMid};`,
        `$malkaBlack: ${theme.surfaceDark};`,
        `$malkaGreen: ${theme.success};`,
        `$malkaRedLight: ${theme.error};`,
        `$malkaRedDark: ${theme.errorDark};`,
        `$malkaOrange: ${theme.accent};`,
        '',
    ];
    return lines.join('\n');
}

const scssContent = generateSCSS(config.theme);
const scssPath = path.join(PROJECT_ROOT, 'css', '_tenant-variables.scss');
fs.writeFileSync(scssPath, scssContent, 'utf8');
console.log(`[whitelabel] Generated: css/_tenant-variables.scss`);

// ---------------------------------------------------------------------------
// 2. Generate whitelabel-runtime.js
// ---------------------------------------------------------------------------
function generateRuntimeJS(cfg) {
    // Flatten features for easy consumption
    const features = {};
    for (const [key, val] of Object.entries(cfg.features)) {
        if (key === 'languages') {
            features.languages = val.enabled;
            features.defaultLanguage = val.default;
        } else if (typeof val === 'object' && 'enabled' in val) {
            features[key] = val.enabled;
        } else {
            features[key] = val;
        }
    }

    const assets = qualifyAssets(cfg.assets || {});

    const runtimeConfig = {
        tenantId: cfg.tenantId,
        appName: cfg.brand.appName,
        providerName: cfg.brand.providerName,
        tagline: cfg.brand.tagline,
        description: cfg.brand.description,
        supportUrl: cfg.brand.supportUrl,
        domains: cfg.domains || {},
        theme: cfg.theme,
        features,
        assets,
        operations: cfg.operations || {}
    };

    return [
        '// Auto-generated by whitelabel-prebuild.js — DO NOT EDIT',
        `// Tenant: ${tenantId}`,
        '',
        `window.__WHITELABEL__ = ${JSON.stringify(runtimeConfig, null, 4)};`,
        ''
    ].join('\n');
}

const runtimeContent = generateRuntimeJS(config);
const runtimePath = path.join(PROJECT_ROOT, 'whitelabel-runtime.js');
fs.writeFileSync(runtimePath, runtimeContent, 'utf8');
console.log(`[whitelabel] Generated: whitelabel-runtime.js`);

// ---------------------------------------------------------------------------
// 3. Generate title.html
// ---------------------------------------------------------------------------
function generateTitleHTML(cfg) {
    const logoAsset = cfg.assets.logo || 'malka-logo.png';
    const socialAsset = cfg.assets.socialImage || logoAsset;
    const socialImagePath = `images/${socialAsset}?v=2`;
    const socialImage = cfg.brand.inviteDomain
        ? `https://${cfg.brand.inviteDomain}/${socialImagePath}`
        : socialImagePath;

    return [
        `<title>${cfg.brand.appName}</title>`,
        `<meta property="og:title" content="${cfg.brand.appName}"/>`,
        `<meta property="og:type" content="website"/>`,
        `<meta property="og:image" content="${socialImage}"/>`,
        `<meta property="og:description" content="${cfg.brand.description}"/>`,
        `<meta name="twitter:card" content="summary_large_image"/>`,
        `<meta name="twitter:title" content="${cfg.brand.appName}"/>`,
        `<meta name="twitter:description" content="${cfg.brand.description}"/>`,
        `<meta name="twitter:image" content="${socialImage}"/>`,
        `<meta description="${cfg.brand.description}"/>`,
        `<meta name="description" content="${cfg.brand.description}"/>`,
        `<meta itemprop="name" content="${cfg.brand.appName}"/>`,
        `<meta itemprop="description" content="${cfg.brand.description}"/>`,
        `<meta itemprop="image" content="${socialImage}"/>`,
        `<link rel="icon" href="images/${cfg.assets.favicon}?v=1">`
    ].join('\n');
}

const titleContent = generateTitleHTML(config);
const titlePath = path.join(PROJECT_ROOT, 'title.html');
fs.writeFileSync(titlePath, titleContent, 'utf8');
console.log(`[whitelabel] Generated: title.html`);

// ---------------------------------------------------------------------------
// 4. Generate manifest.json
// ---------------------------------------------------------------------------
function assetMimeType(assetPath) {
    return assetPath.endsWith('.svg') ? 'image/svg+xml' : 'image/png';
}

function manifestPath(assetPath) {
    return qualifyAssetPath(assetPath || '');
}

function generateManifest(cfg) {
    const mobile = cfg.assets.mobile || {};
    const appIcon = mobile.appIcon || cfg.assets.appleTouchIcon || cfg.assets.favicon32 || cfg.assets.faviconSvg || cfg.assets.favicon;
    const maskableIcon = mobile.maskableIcon || appIcon;

    return {
        android_package_name: cfg.mobile?.androidPackageName || 'org.malka.meet',
        prefer_related_applications: true,
        related_applications: [
            {
                id: cfg.mobile?.androidPackageName || 'org.malka.meet',
                platform: 'chromeos_play'
            }
        ],
        short_name: cfg.brand.appName,
        name: cfg.brand.appName,
        icons: [
            {
                src: manifestPath(appIcon),
                type: assetMimeType(appIcon),
                sizes: '192x192'
            },
            {
                src: manifestPath(appIcon),
                type: assetMimeType(appIcon),
                sizes: '512x512'
            },
            {
                src: manifestPath(maskableIcon),
                sizes: '512x512',
                type: assetMimeType(maskableIcon),
                purpose: 'maskable'
            }
        ],
        start_url: '/',
        background_color: cfg.theme.primary,
        display: 'standalone',
        scope: '/',
        theme_color: cfg.theme.primary
    };
}

const manifestPathOut = path.join(PROJECT_ROOT, 'manifest.json');
fs.writeFileSync(manifestPathOut, JSON.stringify(generateManifest(config), null, 2) + '\n', 'utf8');
console.log(`[whitelabel] Generated: manifest.json`);

// ---------------------------------------------------------------------------
// 5. Copy tenant assets into images/
// ---------------------------------------------------------------------------
const tenantAssetsDir = path.join(WHITELABEL_DIR, tenantId, 'assets');
const imagesDir = path.join(PROJECT_ROOT, 'images');

if (fs.existsSync(tenantAssetsDir)) {
    const files = collectReferencedAssetFiles(config.assets || {});
    for (const file of files) {
        if (file.startsWith('.') || file.startsWith('._')) {
            continue;
        }
        const src = path.join(tenantAssetsDir, file);
        if (!fs.existsSync(src) || !fs.statSync(src).isFile()) {
            continue;
        }
        const dest = path.join(imagesDir, file);
        fs.copyFileSync(src, dest);
        console.log(`[whitelabel] Copied asset: images/${file}`);
    }
} else {
    console.log(`[whitelabel] No custom assets directory for tenant: ${tenantId} (using defaults)`);
}

// ---------------------------------------------------------------------------
// 6. Patch native mobile bundle IDs (iOS + Android) if mobile config exists
// ---------------------------------------------------------------------------
const mobileConfig = config.mobile;

if (mobileConfig) {
    // --- iOS: patch Info.plist and pbxproj ---
    const iosAppDir = path.join(PROJECT_ROOT, 'ios', 'app');
    const iosInfoPlist = path.join(PROJECT_ROOT, 'ios', 'app', 'src', 'Info.plist');
    if (fs.existsSync(iosInfoPlist)) {
        let plist = fs.readFileSync(iosInfoPlist, 'utf8');

        // Update CFBundleDisplayName
        plist = plist.replace(
            /(<key>CFBundleDisplayName<\/key>\s*<string>)[^<]*(<\/string>)/,
            `$1${mobileConfig.displayName}$2`
        );

        fs.writeFileSync(iosInfoPlist, plist, 'utf8');
        console.log(`[whitelabel] Patched ios/app/src/Info.plist (displayName: ${mobileConfig.displayName})`);
    }

    // Patch pbxproj bundle identifier
    const pbxprojFiles = fs.existsSync(iosAppDir)
        ? fs.readdirSync(iosAppDir).filter(f => f.endsWith('.pbxproj'))
        : [];

    for (const pbxFile of pbxprojFiles) {
        const pbxPath = path.join(PROJECT_ROOT, 'ios', 'app', pbxFile);
        let pbx = fs.readFileSync(pbxPath, 'utf8');

        // Replace PRODUCT_BUNDLE_IDENTIFIER for the main app target
        pbx = pbx.replace(
            /PRODUCT_BUNDLE_IDENTIFIER = org\.jitsi\.meet;/g,
            `PRODUCT_BUNDLE_IDENTIFIER = ${mobileConfig.iosBundleId};`
        );

        // Replace broadcast extension bundle ID
        const broadcastBundleId = `${mobileConfig.iosBundleId}.broadcast`;
        pbx = pbx.replace(
            /PRODUCT_BUNDLE_IDENTIFIER = org\.jitsi\.meet\.broadcast\.extension;/g,
            `PRODUCT_BUNDLE_IDENTIFIER = ${broadcastBundleId}.extension;`
        );

        fs.writeFileSync(pbxPath, pbx, 'utf8');
        console.log(`[whitelabel] Patched ios/app/${pbxFile} (bundleId: ${mobileConfig.iosBundleId})`);
    }

    // --- Android: patch build.gradle ---
    const androidBuildGradle = path.join(PROJECT_ROOT, 'android', 'app', 'build.gradle');
    if (fs.existsSync(androidBuildGradle)) {
        let gradle = fs.readFileSync(androidBuildGradle, 'utf8');

        // Replace applicationId
        gradle = gradle.replace(
            /applicationId\s+'org\.jitsi\.meet'/,
            `applicationId '${mobileConfig.androidApplicationId}'`
        );

        // Replace namespace
        gradle = gradle.replace(
            /namespace\s+'org\.jitsi\.meet'/,
            `namespace '${mobileConfig.androidApplicationId}'`
        );

        fs.writeFileSync(androidBuildGradle, gradle, 'utf8');
        console.log(`[whitelabel] Patched android/app/build.gradle (applicationId: ${mobileConfig.androidApplicationId})`);
    }

    // --- Android: patch strings.xml for app name ---
    const stringsXml = path.join(PROJECT_ROOT, 'android', 'app', 'src', 'main', 'res', 'values', 'strings.xml');
    if (fs.existsSync(stringsXml)) {
        let strings = fs.readFileSync(stringsXml, 'utf8');
        strings = strings.replace(
            /(<string name="app_name">)[^<]*(<\/string>)/,
            `$1${mobileConfig.displayName}$2`
        );
        fs.writeFileSync(stringsXml, strings, 'utf8');
        console.log(`[whitelabel] Patched android strings.xml (app_name: ${mobileConfig.displayName})`);
    }
}

// ---------------------------------------------------------------------------
// Done
// ---------------------------------------------------------------------------
console.log(`[whitelabel] Prebuild complete for tenant: ${tenantId}`);

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------
function listTenants() {
    if (!fs.existsSync(WHITELABEL_DIR)) return [];
    return fs.readdirSync(WHITELABEL_DIR)
        .filter(f => f.endsWith('.json'))
        .map(f => f.replace('.json', ''));
}

function qualifyAssetPath(assetPath) {
    if (!assetPath || typeof assetPath !== 'string') {
        return '';
    }
    if (/^(https?:)?\/\//.test(assetPath) || assetPath.startsWith('/') || assetPath.startsWith('data:')) {
        return assetPath;
    }
    if (assetPath.startsWith('static/')) {
        return assetPath;
    }
    return `images/${assetPath}`;
}

function qualifyAssets(value) {
    if (Array.isArray(value)) {
        return value.map(qualifyAssets);
    }
    if (value && typeof value === 'object') {
        return Object.fromEntries(
            Object.entries(value).map(([key, val]) => [key, qualifyAssets(val)])
        );
    }
    return qualifyAssetPath(value);
}

function collectReferencedAssetFiles(value, files = new Set()) {
    if (Array.isArray(value)) {
        value.forEach(item => collectReferencedAssetFiles(item, files));
        return files;
    }
    if (value && typeof value === 'object') {
        Object.values(value).forEach(item => collectReferencedAssetFiles(item, files));
        return files;
    }
    if (typeof value !== 'string' || !value) {
        return files;
    }
    if (/^(https?:)?\/\//.test(value) || value.startsWith('/') || value.startsWith('data:') || value.startsWith('static/')) {
        return files;
    }
    files.add(value);
    return files;
}
