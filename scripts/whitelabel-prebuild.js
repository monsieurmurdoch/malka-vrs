#!/usr/bin/env node

/**
 * Whitelabel Prebuild Script
 *
 * Reads the tenant config (TENANT env var, defaults to "malka") and generates:
 *   - css/_tenant-variables.scss  — SCSS color overrides for the tenant
 *   - whitelabel-runtime.js       — JS runtime config (appName, features, assets)
 *   - title.html                  — Branded <title> and meta tags
 *
 * Also copies tenant assets from whitelabel/{tenant}/assets/ into images/.
 */

'use strict';

const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = path.resolve(__dirname, '..');
const WHITELABEL_DIR = path.join(PROJECT_ROOT, 'whitelabel');

// ---------------------------------------------------------------------------
// Resolve tenant
// ---------------------------------------------------------------------------
const tenantId = process.env.TENANT || 'malka';
const configPath = path.join(WHITELABEL_DIR, `${tenantId}.json`);

if (!fs.existsSync(configPath)) {
    console.error(`[whitelabel] ERROR: Tenant config not found: ${configPath}`);
    console.error(`[whitelabel] Available tenants: ${listTenants().join(', ')}`);
    process.exit(1);
}

const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
console.log(`[whitelabel] Building for tenant: ${tenantId} (${config.brand.appName})`);

// ---------------------------------------------------------------------------
// 1. Generate css/_tenant-variables.scss
// ---------------------------------------------------------------------------
function generateSCSS(theme) {
    const lines = [
        '// Auto-generated by whitelabel-prebuild.js — DO NOT EDIT',
        `// Tenant: ${tenantId}`,
        '',
        `$malkaRoyalPurple: ${theme.primary};`,
        `$malkaLightPurple: ${theme.primaryLight};`,
        `$malkaDarkPurple: ${theme.primaryDark};`,
        `$malkaWhite: ${theme.surfaceLight};`,
        `$malkaDarkGrey: ${theme.surfaceMid};`,
        `$malkaBlack: ${theme.surfaceDark};`,
        `$malkaGreen: ${theme.success};`,
        `$malkaRedLight: ${theme.error};`,
        `$malkaRedDark: ${theme.errorDark};`,
        `$malkaOrange: ${theme.accent};`,
        '',
    ];
    return lines.join('\n');
}

const scssContent = generateSCSS(config.theme);
const scssPath = path.join(PROJECT_ROOT, 'css', '_tenant-variables.scss');
fs.writeFileSync(scssPath, scssContent, 'utf8');
console.log(`[whitelabel] Generated: css/_tenant-variables.scss`);

// ---------------------------------------------------------------------------
// 2. Generate whitelabel-runtime.js
// ---------------------------------------------------------------------------
function generateRuntimeJS(cfg) {
    // Flatten features for easy consumption
    const features = {};
    for (const [key, val] of Object.entries(cfg.features)) {
        if (key === 'languages') {
            features.languages = val.enabled;
            features.defaultLanguage = val.default;
        } else if (typeof val === 'object' && 'enabled' in val) {
            features[key] = val.enabled;
        } else {
            features[key] = val;
        }
    }

    const assets = {};
    for (const [key, val] of Object.entries(cfg.assets)) {
        assets[key] = val ? `images/${val}` : '';
    }

    const runtimeConfig = {
        tenantId: cfg.tenantId,
        appName: cfg.brand.appName,
        providerName: cfg.brand.providerName,
        tagline: cfg.brand.tagline,
        description: cfg.brand.description,
        supportUrl: cfg.brand.supportUrl,
        theme: cfg.theme,
        features,
        assets
    };

    return [
        '// Auto-generated by whitelabel-prebuild.js — DO NOT EDIT',
        `// Tenant: ${tenantId}`,
        '',
        `window.__WHITELABEL__ = ${JSON.stringify(runtimeConfig, null, 4)};`,
        ''
    ].join('\n');
}

const runtimeContent = generateRuntimeJS(config);
const runtimePath = path.join(PROJECT_ROOT, 'whitelabel-runtime.js');
fs.writeFileSync(runtimePath, runtimeContent, 'utf8');
console.log(`[whitelabel] Generated: whitelabel-runtime.js`);

// ---------------------------------------------------------------------------
// 3. Generate title.html
// ---------------------------------------------------------------------------
function generateTitleHTML(cfg) {
    const logoAsset = cfg.assets.logo || 'malka-logo.png';
    const socialAsset = cfg.assets.socialImage || logoAsset;
    const socialImagePath = `images/${socialAsset}?v=2`;
    const socialImage = cfg.brand.inviteDomain
        ? `https://${cfg.brand.inviteDomain}/${socialImagePath}`
        : socialImagePath;

    return [
        `<title>${cfg.brand.appName}</title>`,
        `<meta property="og:title" content="${cfg.brand.appName}"/>`,
        `<meta property="og:type" content="website"/>`,
        `<meta property="og:image" content="${socialImage}"/>`,
        `<meta property="og:description" content="${cfg.brand.description}"/>`,
        `<meta name="twitter:card" content="summary_large_image"/>`,
        `<meta name="twitter:title" content="${cfg.brand.appName}"/>`,
        `<meta name="twitter:description" content="${cfg.brand.description}"/>`,
        `<meta name="twitter:image" content="${socialImage}"/>`,
        `<meta description="${cfg.brand.description}"/>`,
        `<meta name="description" content="${cfg.brand.description}"/>`,
        `<meta itemprop="name" content="${cfg.brand.appName}"/>`,
        `<meta itemprop="description" content="${cfg.brand.description}"/>`,
        `<meta itemprop="image" content="${socialImage}"/>`,
        `<link rel="icon" href="images/${cfg.assets.favicon}?v=1">`
    ].join('\n');
}

const titleContent = generateTitleHTML(config);
const titlePath = path.join(PROJECT_ROOT, 'title.html');
fs.writeFileSync(titlePath, titleContent, 'utf8');
console.log(`[whitelabel] Generated: title.html`);

// ---------------------------------------------------------------------------
// 4. Copy tenant assets into images/
// ---------------------------------------------------------------------------
const tenantAssetsDir = path.join(WHITELABEL_DIR, tenantId, 'assets');
const imagesDir = path.join(PROJECT_ROOT, 'images');

if (fs.existsSync(tenantAssetsDir)) {
    const files = fs.readdirSync(tenantAssetsDir);
    for (const file of files) {
        if (file.startsWith('.') || file.startsWith('._')) {
            continue;
        }
        const src = path.join(tenantAssetsDir, file);
        if (!fs.statSync(src).isFile()) {
            continue;
        }
        const dest = path.join(imagesDir, file);
        fs.copyFileSync(src, dest);
        console.log(`[whitelabel] Copied asset: images/${file}`);
    }
} else {
    console.log(`[whitelabel] No custom assets directory for tenant: ${tenantId} (using defaults)`);
}

// ---------------------------------------------------------------------------
// Done
// ---------------------------------------------------------------------------
console.log(`[whitelabel] Prebuild complete for tenant: ${tenantId}`);

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------
function listTenants() {
    if (!fs.existsSync(WHITELABEL_DIR)) return [];
    return fs.readdirSync(WHITELABEL_DIR)
        .filter(f => f.endsWith('.json'))
        .map(f => f.replace('.json', ''));
}
